export function getDailyTip(): string {
  // حساب اليوم من بداية السنة
  const now = new Date()
  const start = new Date(now.getFullYear(), 0, 0)
  const diff = now.getTime() - start.getTime()
  const oneDay = 1000 * 60 * 60 * 24
  const dayOfYear = Math.floor(diff / oneDay)

  // اختيار نصيحة بناءً على اليوم
  const tipIndex = dayOfYear % DAILY_TIPS.length

  return DAILY_TIPS[tipIndex]
}

export function checkIfDailyTipRequest(message: string): boolean {
  const normalized = message.toLowerCase().trim()
  return (
    normalized.includes("نصيحة اليوم") ||
    normalized.includes("نصيحه اليوم") ||
    normalized === "نصيحة اليوم 💡" ||
    normalized === "نصيحة اليوم"
  )
}
